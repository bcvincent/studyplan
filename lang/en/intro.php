<p>
Your personal study plan is based on the results of your quiz.
</p>
<p>
The areas where you are strong have a check mark beside them.
You may choose to review these areas later if you like, but you should focus your time on the areas where your skills need improvement.
</p>
<p>
The color codes will help you know what to do next.
The lessons or activities in green are the ones that your test indicated you need to study.
Your teacher can also assign lessons.
Those lessons are in blue.
</p>
<p>
As you complete your assigned lessons they will show up on your study plan with a check mark. 
This will help you keep track of your progress.
</p>
<p>
It is important that you move through your study plan in order, because each lesson builds on skills from the previous ones.
</p>